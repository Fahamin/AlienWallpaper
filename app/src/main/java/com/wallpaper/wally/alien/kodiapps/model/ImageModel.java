package com.wallpaper.wally.alien.kodiapps.model;

public class ImageModel {

    int i;
    String l;

    public ImageModel() {
    }

    public ImageModel(int i, String l) {
        this.i = i;
        this.l = l;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getL() {
        return l;
    }

    public void setL(String l) {
        this.l = l;
    }
}
